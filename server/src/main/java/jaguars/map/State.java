package jaguars.map;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class State {
    @Id
    int id;
    String name;
    int year;

    public State() {
    }

    public State(int id, String name, int year) {
        this.id = id;
        this.name = name;
        this.year = year;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
